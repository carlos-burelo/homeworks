package PrimerParcial;

public class Operaciones {
    public static void main(String[] args) {
        final int A = 10;
        final int B = 5;
        int suma = A + B;
        int resta = A - B;
        int multiplicacion = A * B;
        int division = A / B;
        System.out.println("La suma de 10 y 5 es: " + suma);
        System.out.println("La resta de 10 y 5 es: " + resta);
        System.out.println("La multiplicacion de 10 y 5 es: " + multiplicacion);
        System.out.println("La division de 10 y 5 es: " + division);
    }
}
